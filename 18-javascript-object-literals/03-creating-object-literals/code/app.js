// Creating Object Literals

// Object literal with properties of type string
const person = {
  firstName: "Mick",
  lastName: "Jagger",
};

// Access the object literal "person"
console.log(person);

// Object literal with different types of properties
const kitchenSink = {
  faveNum: 93219023,
  isFunny: true,
  colors: ["red", "orange"],
};

// Access the object literal "kitchenSink"
console.log(kitchenSink);
