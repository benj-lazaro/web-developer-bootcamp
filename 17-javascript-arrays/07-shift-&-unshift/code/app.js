// Array Methods Shift & Unshift

let movieLine = ["tom", "nancy", "oliver", "eva"];
console.log(movieLine);

let shiftedPerson = movieLine.shift();
console.log(movieLine);
console.log(shiftedPerson);

shiftedPerson = movieLine.shift();
console.log(movieLine);
console.log(shiftedPerson);

movieLine.push("jerry", "deniz", "kevin", "carly");
console.log(movieLine);

importantPerson = movieLine.unshift("VIP");
console.log(movieLine);
